class SplashScreen extends StatefulWidget {
  const SplashScreen({super.key});

  @override
  State<SplashScreen> createState() => _SplashScreenState();
}

class _SplashScreenState extends State<SplashScreen> with SingleTickerProviderStateMixin {
  bool _showStartButton = false;
  bool _showLoginButtons = false;

  @override
  void initState() {
    super.initState();

    // ÏãúÏûëÌïòÍ∏∞ Î≤ÑÌäºÏùÄ 3Ï¥à ÌõÑ ÌëúÏãú
    Timer(const Duration(seconds: 3), () {
      if (mounted) {
        setState(() {
          _showStartButton = true;
        });
      }
    });
  }

  void _handleUserInteraction() {
    if (!_showLoginButtons) {
      setState(() {
        _showLoginButtons = true;
      });
    }
  }

  void _handleGuestAccess() async {
    final url = Uri.parse('https://zae-park.github.io/zae-labeler/');
    showDialog(
      context: context,
      builder: (_) => AlertDialog(
        title: const Text("ÎπÑÌöåÏõê ÏïàÎÇ¥"),
        content: const Text("ÎπÑÌöåÏõê Î™®Îìú Ïù¥Ïö© Ïãú ÏßÑÌñâ ÏÇ¨Ìï≠Ïù¥ Ï†ÄÏû•ÎêòÏßÄ ÏïäÏäµÎãàÎã§.\nÏô∏Î∂Ä ÎßÅÌÅ¨Î°ú Ïù¥ÎèôÌï†ÍπåÏöî?"),
        actions: [
          TextButton(onPressed: () => Navigator.pop(context), child: const Text("Ï∑®ÏÜå")),
          TextButton(
              onPressed: () async {
                Navigator.pop(context);
                if (await canLaunchUrl(url)) {
                  await launchUrl(url, mode: LaunchMode.externalApplication);
                }
              },
              child: const Text("Ïù¥Îèô")),
        ],
      ),
    );
  }

  Future<void> _signInWithGoogle(BuildContext context) async {
    final authVM = context.read<AuthViewModel>();
    await authVM.signInWithGoogle();
    if (authVM.isSignedIn && mounted) {
      Navigator.pushReplacementNamed(context, '/project_list');
    }
  }

  Future<void> _signInWithGitHub(BuildContext context) async {
    final authVM = context.read<AuthViewModel>();
    await authVM.signInWithGitHub();
    if (authVM.isSignedIn && mounted) {
      Navigator.pushReplacementNamed(context, '/project_list');
    }
  }

  @override
  Widget build(BuildContext context) {
    final authVM = context.watch<AuthViewModel>();

    return GestureDetector(
      onTap: _handleUserInteraction,
      child: Scaffold(
        backgroundColor: Colors.black,
        body: SafeArea(
          child: AutoSeparatedColumn(
            separator: const SizedBox(height: 16),
            children: [
              Expanded(
                child: Center(
                  child: Image.asset('assets/zae-splash2.gif', width: 250, height: 250, fit: BoxFit.contain),
                  // child: Lottie.asset('assets/zae-splash.json', width: 250, height: 250, fit: BoxFit.contain),
                ),
              ),
              const Text("ZAE Labeler", style: TextStyle(color: Colors.white, fontSize: 24, fontWeight: FontWeight.bold)),
              SizedBox(
                height: 200,
                child: Stack(
                  alignment: Alignment.center,
                  children: [
                    // ÏãúÏûëÌïòÍ∏∞ Î≤ÑÌäº (fade out ‚Üí onEndÏóêÏÑú Î°úÍ∑∏Ïù∏ Î≤ÑÌäº Î≥¥Ïó¨Ï£ºÍ∏∞)
                    AnimatedOpacity(
                      opacity: _showLoginButtons ? 0.0 : (_showStartButton ? 1.0 : 0.0),
                      duration: const Duration(milliseconds: 600),
                      child: Visibility(
                        visible: !_showLoginButtons,
                        child: ElevatedButton(
                          style: ElevatedButton.styleFrom(
                            backgroundColor: Colors.transparent, // üîç Î∞∞Í≤Ω Ìà¨Î™Ö
                            foregroundColor: Colors.grey[300], // üîç Í∏ÄÏûê ÌöåÏÉâ
                            elevation: 0,

                            padding: const EdgeInsets.symmetric(horizontal: 32, vertical: 16),
                          ),
                          onPressed: () {
                            setState(() => _showStartButton = false);
                            Future.delayed(const Duration(milliseconds: 300), () {
                              if (mounted) setState(() => _showLoginButtons = true);
                            });
                          },
                          child: const Text("ÏãúÏûëÌïòÍ∏∞", style: TextStyle(fontSize: 20, fontWeight: FontWeight.bold)),
                        ),
                      ),
                    ),

                    // Î°úÍ∑∏Ïù∏ Î≤ÑÌäºÎì§ (fade-in)
                    AnimatedOpacity(
                      opacity: _showLoginButtons ? 1.0 : 0.0,
                      duration: const Duration(milliseconds: 600),
                      child: Visibility(
                        visible: _showLoginButtons,
                        child: AutoSeparatedColumn(
                          separator: const SizedBox(height: 16),
                          children: [
                            if (authVM.conflictingEmail != null)
                              Padding(
                                padding: const EdgeInsets.all(12.0),
                                child: Text(
                                  "‚ö†Ô∏è ${authVM.conflictingEmail} Í≥ÑÏ†ïÏùÄ Ïù¥ÎØ∏ Í∞ÄÏûÖÎêòÏñ¥ ÏûàÏóàÏäµÎãàÎã§. Îã§Î•∏ Î∞©Î≤ïÏúºÎ°ú Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.",
                                  style: const TextStyle(color: Colors.yellow, fontWeight: FontWeight.bold),
                                  textAlign: TextAlign.center,
                                ),
                              ),
                            ElevatedButton.icon(icon: const Icon(Icons.login), label: const Text("GoogleÎ°ú Î°úÍ∑∏Ïù∏"), onPressed: () => _signInWithGoogle(context)),
                            ElevatedButton.icon(icon: const Icon(Icons.code), label: const Text("GitHubÎ°ú Î°úÍ∑∏Ïù∏"), onPressed: () => _signInWithGitHub(context)),
                            TextButton.icon(icon: const Icon(Icons.open_in_new), label: const Text("ÎπÑÌöåÏõêÏúºÎ°ú Ïù¥Ïö©ÌïòÍ∏∞"), onPressed: _handleGuestAccess),
                          ],
                        ),
                      ),
                    ),
                  ],
                ),
              ),
              const SizedBox(height: 16),
            ],
          ),
        ),
      ),
    );
  }
}
